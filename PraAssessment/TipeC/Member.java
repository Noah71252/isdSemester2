package TipeC;

public class Member {
    String noMember;
    String nama;
    String alamat;
    String tipeMember;
    int lamaMenjadiMember;

    public Member(String noMember, String nama, String alamat, String tipeMember, int lamaMenjadiMember) {
        this.noMember = noMember;
        this.nama = nama;
        this.alamat = alamat;
        this.tipeMember = tipeMember;
        this.lamaMenjadiMember = lamaMenjadiMember;
    }
}
